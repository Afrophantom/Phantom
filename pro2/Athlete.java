package pro2;

public abstract class Athlete {
	
	private String myName;
	private int myAge;
	public Athlete(String name, int age){
		myName=name;
		myAge=age;
	
	}
	
	




	public String getMyName() {
		return myName;
	}
	public int getMyAge() {
		return myAge;
	}
	
	
	
	
}